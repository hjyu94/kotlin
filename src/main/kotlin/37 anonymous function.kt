/*
    # 익명함수
    - 함수의 이름이 없는 함수
    - 함수를 변수에 담아 관리할 때 사용
    - 고차 함수와 관련이 깊다
 */

fun main() {
    fun37_1()

    // 변수에 함수 이름을 저장할 수 없다.
    // 함수의 이름이 함수 객체의 주소값을 담는 언어들은 이게 가능한데
    // 코틀린은 함수의 이름을 가지고 변수에 담아서 함수의 주소를 저장할 수 없다.
//    val testFunc = fun37_1 // ERROR
    // 대신 이름이 없는 함수를 작성하면 된다.
    val testFun = fun() {
        println(">> testFun()")
    }
    testFun()
}

fun fun37_1() {
    println(">> fun37_1()")
}